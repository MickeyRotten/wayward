export function IconRail({
  activeTab,
  onTabChange,
}: {
  activeTab: TabId
  onTabChange: (tab: TabId) => void
}) {
  return (
    <div className="flex flex-col pt-3">
      {TABS.map((tab) => {
        const isActive = activeTab === tab.id
        return (
          <button
            key={tab.id}
            type="button"
            className={`relative flex flex-col items-center justify-center w-[66px] h-[52px] transition-colors ${
              isActive
                ? 'text-gold'
                : 'text-textdim hover:text-textsec'
            }`}
            onClick={() => onTabChange(tab.id)}
            title={tab.label}
          >
            {/* Active indicator — gold left border */}
            {isActive && (
              <div className="absolute left-0 top-2 bottom-2 w-[2px] bg-gold" />
            )}
            {tab.icon}
            <span className="font-ui text-[8px] tracking-wider mt-1">
              {tab.label.toUpperCase()}
            </span>
          </button>
        )
      })}
    </div>
  )
}
